
//width height(ウィドゥス　ハイト）
・要素の高さと幅を指定する。

//margin (マージン）
・要素間の隙間を調整する際に使用。

//padding　（パディング）
・要素内の余白を調整する際に使用。

//border　（ボーダー）
marginとpaddingの間にある境界線のこと。
border-rediusを使用すると、borderの形状を変更することも可能。

//font-size
fontのサイズを変更するためのCSS

//font-weight
fontの太さを変更するCSS

//font-style
fontのスタイルを変更のCSS

//font-family
fontの変更を行うことができる

//background (バックグラウンド）
要素の背景の変更を行う

//display (ディスプレイ）
divやpのように縦に積み上げられる要素をブロックボックスと言う
spanやaのように横につかされていく要素をインラインボックスと呼ぶ

//position (ポジジョン）
要素の位置を変更したい時、positionプロパティを使用
relative
position relativeを使用することで、相対的に位置を移動することが可能

//relativeの後に、「top」「bottom」「right」「left」を指定し、それぞれの位置を移動

//absolute(アブソルート）
「ウィンド、または親要素」を基準に、そこから距離の場所に配置される

//float (フロート）
・left(左寄せ）
・right(右寄せ）
・none(フロートさせない）
・inherit(継承）
の４つ
